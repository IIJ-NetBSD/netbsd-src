	.text
	.globl	__start
__start:
#include "../../wii/wii_mmuinit.S"

	lis	%r1, STACKADDR@ha
	ori	%r1, %r1, STACKADDR@l

	bl	_C_LABEL(main)

loop:	b	loop		/* not reached */
